package NewPojo;

public class Rating
{
    private String scheme;

    private String rating;

    public void setScheme(String scheme){
        this.scheme = scheme;
    }
    public String getScheme(){
        return this.scheme;
    }
    public void setRating(String rating){
        this.rating = rating;
    }
    public String getRating(){
        return this.rating;
    }
}
