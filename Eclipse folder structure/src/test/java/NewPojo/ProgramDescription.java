package NewPojo;

public class ProgramDescription
{
    private String ar;

    private String en;

    public void setAr(String ar){
        this.ar = ar;
    }
    public String getAr(){
        return this.ar;
    }
    public void setEn(String en){
        this.en = en;
    }
    public String getEn(){
        return this.en;
    }
}